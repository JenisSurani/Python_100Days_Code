pie="ApplePie"
print(pie[:]) # means 0 to till len(pie) 
print(pie[::1]) # print enitre string using slicing with jump index 1 
print(pie[::-1]) # reverse a string with jump index -1 

print(pie[:5]) # means 0 to 5 means total 5 char print karo
print(pie[0:2])
print(pie[3]) # return index value at index 3

print(pie[3:])# from index 3 till end
print(pie[2:5]) # slicing in between

print(pie[-8:]) # print(pie[len(pie)-8]) means 0 till end
print(pie[:-4]) # means 0 till 4 

print(pie[-4:-6]) # means from 4 to 2 that doesn't mean anything means no print or print only /n


